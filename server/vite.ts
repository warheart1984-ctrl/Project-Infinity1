import { type Express } from "express";
import { createServer as createViteServer, createLogger } from "vite";
import { type Server } from "http";
import viteConfig from "../vite.config";
import fs from "fs";
import path from "path";

const viteLogger = createLogger();
let cachedTemplate: string | null = null;
let cachedTemplateMtimeMs = 0;

async function getCachedClientTemplate(clientTemplatePath: string): Promise<string> {
  const stat = await fs.promises.stat(clientTemplatePath);
  if (!cachedTemplate || stat.mtimeMs !== cachedTemplateMtimeMs) {
    cachedTemplate = await fs.promises.readFile(clientTemplatePath, "utf-8");
    cachedTemplateMtimeMs = stat.mtimeMs;
  }

  return cachedTemplate;
}

export async function setupVite(server: Server, app: Express) {
  const serverOptions = {
    middlewareMode: true,
    hmr: { server, path: "/vite-hmr" },
    allowedHosts: true as const,
  };

  const vite = await createViteServer({
    ...viteConfig,
    configFile: false,
    customLogger: {
      ...viteLogger,
      error: (msg, options) => {
        viteLogger.error(msg, options);
        process.exit(1);
      },
    },
    server: serverOptions,
    appType: "custom",
  });

  app.use(vite.middlewares);

  app.use("/{*path}", async (req, res, next) => {
    const url = req.originalUrl;

    try {
      const clientTemplate = path.resolve(
        import.meta.dirname,
        "..",
        "client",
        "index.html",
      );

      const template = await getCachedClientTemplate(clientTemplate);
      const page = await vite.transformIndexHtml(url, template);
      res.status(200).set({ "Content-Type": "text/html" }).end(page);
    } catch (e) {
      vite.ssrFixStacktrace(e as Error);
      next(e);
    }
  });
}
